'''
Desenhe um fluxograma e implemente o programa em Python que recebe o raio da base e a altura de um
cilindro, calcula e mostra seu volume.
'''

print('')
print('VOLUME CILÍNDRO')
print('')

R = float(input('Digite o raio da base: '))
H = float(input('Digite a altura do cilíndro: '))

V = 3.14 * R**2 * H

print('')
print('O volume do cilíndo é:', V)
print('')